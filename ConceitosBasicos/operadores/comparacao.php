<?php

$a = 30;

$b = 55;

//Maior que
var_dump($a > $b);

echo "<br>";
//Menor que
var_dump($a < $b);

echo "<br>";
//Igual(comparação de valor)
var_dump($a == $b);

echo "<br>";
//Igual(comparação de valor e tipo)
var_dump($a === $b);

echo "<br>";
//Diferente(comparação de valor)
var_dump($a != $b);

echo "<br>";
//Diferente(comparação de valor e tipo)
var_dump($a !== $b);

echo "<br>";
?>